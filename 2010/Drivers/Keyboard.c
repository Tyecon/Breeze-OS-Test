short shift=0;
short escaped=0;
unsigned char scancode;

void keyboard_handler()
{
	scancode=inportb(0x60);
	if (escaped==1) {scancode+=256; escaped=0;}
	if (scancode==0x2A || scancode==0x36) {shift=1;}
	if (scancode==0xAA) {shift=0;}
	if (scancode==0xE0) {escaped=1;}
	//if (escaped=1) {Write_Char(kbdus[scancode]);}
	if (scancode & 0x80)
	{
		//Keyup
	} else {
		//Keydown
		if(shift==0) {Write_Char(kbdus[scancode]);} else {Write_Char(kbdusS[scancode]);}
	}
}

void keyboard_install()
{
	irq_install_handler(1, keyboard_handler);
}
